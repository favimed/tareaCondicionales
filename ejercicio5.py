if (dia == "lunes"):
    print ("A empezar la semana con ganas!!")
elif (dia == "viernes"):
    print ("Ya empezamos el finde!")
elif (dia == "sabado" or dia == "domingo"):
    print ("A disfrutar del finde!!")
else:
    print ("Ya queda menos para terminar la semana")
